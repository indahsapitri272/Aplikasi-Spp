    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
         if (txtUsername.getText(). equals("") || txtPassword.getText(). equals("") || txtNamapetugas.getText(). equals("") || cbLevel.getSelectedItem(). equals(""))
        {
            JOptionPane.showMessageDialog(this, "data harus terisi semua");
        }else {
        try {
            PreparedStatement ps = con.prepareStatement("INSERT INTO user VALUES (?,?,?,?,?)");
            ps.setInt(1, 0);
            ps.setString(2, txtUsername.getText());
            ps.setString(3, txtPassword.getText());
            ps.setString(4, txtNamapetugas.getText());
            ps.setString(5, cbLevel.getSelectedItem().toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah tersimpan" );
            
            refreshTable();
            txtUsername.setText("");
            txtPassword.setText("");
            txtNamapetugas.setText("");
            cbLevel.getSelectedItem();
        } catch (Exception e) {
            System.out.print("ERROR KUERI KE DATABSE:\n" + e + "\n\n");
        }
         }
    }//GEN-LAST:event_btnTambahActionPerformed

