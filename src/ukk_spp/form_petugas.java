    private void tbPetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPetugasMouseClicked
        // TODO add your handling code here:
        int i = tbPetugas.getSelectedRow();
        if (i== -1) {
            return;
        }
        
        String Idpetugas = (String) tm.getValueAt(i, 0);
        idpetugas=Idpetugas;
        
        String username = (String) tm.getValueAt(i, 1);
        txtUsername.setText(username);
        
        String password = (String) tm.getValueAt(i, 2);
        txtPassword.setText(password);
        
        String nama_user = (String) tm.getValueAt(i, 3);
        txtNamapetugas.setText(nama_user);
        
        String level = (String) tm.getValueAt(i, 4);
        cbLevel.setSelectedItem(level);
    }//GEN-LAST:event_tbPetugasMouseClicked

